package com.example.scancodemanager.Other;

public class CodeMsgUrl {
    private int code;
    private String msg;
    private UrlData data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public UrlData getData() {
        return data;
    }

    public void setData(UrlData data) {
        this.data = data;
    }

}
